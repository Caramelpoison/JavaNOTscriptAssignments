public class cast {
  
  public static void main(String[] args) {
        double myDouble = 112.35;
        int myInt = (int)myDouble;
        System.out.println("The double value: " + myDouble);
        System.out.println("The integer value: " + myInt);

        String myString = "49";
        int newInt = Integer.parseInt(myString); 

        System.out.println("The string value: " + myString);
        System.out.println("The integer value: " + newInt);





  }}